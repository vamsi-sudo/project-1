//size of datatypes

#include <iostream>

using namespace std;

int main()
{
    cout<<"size of char: "<<sizeof(char)<<"byte"<<endl;
    cout<<"size of int: "<<sizeof(int)<<"byte"<<endl;
    cout<<"size of float: "<<sizeof(float)<<"bytes"<<endl;
    cout<<"size of double: "<<sizeof(double)<<"bytes";
}


